    private void cargarDatos(String filtro) {
        DaoMoto daoMoto = new DaoMoto();
        ResultSet clientes = daoMoto.getMotos(filtro);
        int size, i = 0;
        try {
            if (clientes != null) {
                // Obtiene el tama√±o del ResultSet (Numero de registros).
                clientes.last(); // Mueve el puntero al ultimo dato
                size = clientes.getRow(); // Obtiene el numero de la fila
                clientes.beforeFirst(); // Se mueve al inicio
                
                DefaultTableModel tableModel = new DefaultTableModel(new String []{"Id", "Modelo", "Linea", "Cilindraje"}, size);
                tblClientes.setModel(tableModel);
                
                //Recorrido de datos...
                while (clientes.next()) {
                    tableModel.setValueAt(clientes.getString("Id"), i, 0);
                    tableModel.setValueAt(clientes.getString("Modelo"), i, 1);
                    tableModel.setValueAt(clientes.getString("Linea"), i, 2);
                    tableModel.setValueAt(clientes.getString("Cilindraje"), i, 3);
                    i++;
                }
            } else if (!filtro.equals("")) {
                JOptionPane.showMessageDialog(this, "Error: No exiten registros en la base de datos");
            }
        } catch (SQLException ex) {
            System.out.println("Error: No se pudo consultar la lista de clientes: "+ ex);
        }
    }
