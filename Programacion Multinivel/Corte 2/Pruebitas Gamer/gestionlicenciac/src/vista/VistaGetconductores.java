    private void ButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonUpdateActionPerformed
        
        int res = JOptionPane.showConfirmDialog(null, "SI ACEPTA SE SOBREESCRIBIRA LA INFORAMCION DEL USUARIO");
        
        if(res == 0){
            
        int doi;

        boolean sw = false;

        doi = (Integer.parseInt(txt_doi.getText()));

        for (Conductor ob : Data.lstConductores) {

            if (ob.getDoi() == doi) {

                if (!"".equals(txt_doi.getText())) {
                    ob.setDoi(Integer.parseInt(txt_doi.getText()));
                }
                if (!"".equals(txt_nbr.getText())) {
                    ob.setNbr(txt_nbr.getText());
                }
                if (!"".equals(txt_sex.getText())) {
                    ob.setSex(txt_sex.getText());
                }
                if (!"".equals(txt_lic.getText())) {
                    ob.setLicc(txt_lic.getText());
                }
                if (!"".equals(txt_cat.getText())) {
                    ob.setCat(txt_cat.getText());
                }
                if (!"".equals(txt_fev.getText())) {
                    ob.setFev(LocalDate.parse(txt_fev.getText()));
                }
                sw = true;
            }
        }
        limpiar();

        if (!sw) {

            JOptionPane.showMessageDialog(null, "Documento no encotrado");
            limpiar();

        }
        }else{
            limpiar();
        }
        

    }//GEN-LAST:event_ButtonUpdateActionPerformed

