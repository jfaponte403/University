    private void ButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonInsertActionPerformed

        Conductor ob = new Conductor();
        
        ob.setDoi(Integer.parseInt(txt_doi.getText()));
        ob.setNbr(txt_nbr.getText());
        ob.setSex(txt_sex.getText());
        ob.setLicc(txt_lic.getText());
        ob.setCat(txt_cat.getText());
        ob.setFev(LocalDate.parse(txt_fev.getText()));
        Data.lstConductores.add(ob);
        
        JOptionPane.showMessageDialog(null, "Registro guardado exitosamente");
        
        limpiar();
    }//GEN-LAST:event_ButtonInsertActionPerformed

    private void ButtonConsultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConsultActionPerformed

        int doi;

        boolean sw = false;

        doi = (Integer.parseInt(txt_doi.getText()));

        for (Conductor ob : Data.lstConductores) {

            if (ob.getDoi() == doi) {

                txt_doi.setText(String.valueOf(ob.getDoi()));
                txt_nbr.setText(ob.getNbr());
                txt_sex.setText(ob.getSex());
                txt_lic.setText(ob.getLicc());
                txt_cat.setText(ob.getCat());
                txt_fev.setText(String.valueOf(ob.getFev()));
                sw = true;

            }
        }

        if (!sw) {

            JOptionPane.showMessageDialog(null, "Documento no encotrado");
            limpiar();

        }

    }//GEN-LAST:event_ButtonConsultActionPerformed

